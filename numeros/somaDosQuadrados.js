//Elaborar um programa que efetue a leitura de três valores (A, B e C) e apresente como resultado final à soma dos quadrados dos três valores lidos

let a = Number(prompt("Qual o valor de a?"))
let b = Number(prompt("Qual o valor de b?")) 
let c = Number(prompt("Qual o valor de c?"))
let somaQuadrados = (a*a)+(b*b)+(c*c)
alert("A soma dos quadrados dos valores é: " + somaQuadrados)
